@Component({
  selector: 'alk-categories',
  templateUrl: './categories.component.html',
  styleUrls: ['./categories.component.scss'],
})
export class CategoriesComponent implements OnInit {
  categories!: ICategory[];

  constructor(public spinner$: SpinnerService, private route: Router) {}

  ngOnInit(): void {
    this.categories = mockCategories;
  }

  deleteCategory(id: number) {
    const item = document.getElementById(id.toString());
    item?.classList.add('animate__animated', 'animate__backOutRight');
    setTimeout(() => {
      this.categories = this.categories.filter((news) => news.id != id);
    }, 600);
  }

  createCategory() {
    this.route.navigate(['backoffice/categorias/crear']);
  }

  editCategory(id: number) {
    this.route.navigate(['backoffice/categorias/editar', id]);
  }
}

